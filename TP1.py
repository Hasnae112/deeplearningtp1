# ─────────────────────────────────────────
# 3. Perceptron FROM SCRATCH
# ─────────────────────────────────────────
class PerceptronScratch:
    def __init__(self, lr=0.01, n_epochs=100):
        self.lr = lr
        self.n_epochs = n_epochs
        self.weights = None
        self.bias = None

    def _step(self, z):
        return np.where(z >= 0, 1, 0)

    def fit(self, X, y):
        n_samples, n_features = X.shape
        # i. Initialisation des poids et du biais
        self.weights = np.zeros(n_features)
        self.bias = 0
        self.errors_ = []

        for _ in range(self.n_epochs):
            errors = 0
            for xi, yi in zip(X, y):
                # ii. Calcul de la sortie
                z = np.dot(xi, self.weights) + self.bias
                y_pred = self._step(z)
                # iii. Mise à jour des poids
                update = self.lr * (yi - y_pred)
                self.weights += update * xi
                self.bias    += update
                errors += int(update != 0)
            self.errors_.append(errors)

    def predict(self, X):
        z = np.dot(X, self.weights) + self.bias
        return self._step(z)

    def accuracy(self, X, y):
        return np.mean(self.predict(X) == y)


perceptron = PerceptronScratch(lr=0.01, n_epochs=50)
perceptron.fit(X_train, y_train)

acc = perceptron.accuracy(X_test, y_test)
print(f"[From Scratch] Accuracy sur le test : {acc * 100:.2f}%")

