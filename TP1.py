import numpy as np

# Fonction d'activation (step function)
def activation(x):
    return 1 if x >= 0 else 0

# Entraînement du perceptron
def train_perceptron(X, y, epochs=10, lr=0.1):
    weights = np.zeros(len(X[0]))
    bias = 0

    for _ in range(epochs):
        for i in range(len(X)):
            linear_output = np.dot(X[i], weights) + bias
            y_pred = activation(linear_output)

            # mise à jour
            error = y[i] - y_pred
            weights += lr * error * X[i]
            bias += lr * error

    return weights, bias

# Prédiction
def predict(X, weights, bias):
    y_pred = []
    for x in X:
        linear_output = np.dot(x, weights) + bias
        y_pred.append(activation(linear_output))
    return y_pred


# Données
X = np.array([[0,0], [0,1], [1,0], [1,1]])

y_and = np.array([0,0,0,1])
y_or  = np.array([0,1,1,1])

# AND
w_and, b_and = train_perceptron(X, y_and)
print("AND predictions:", predict(X, w_and, b_and))

# OR
w_or, b_or = train_perceptron(X, y_or)
print("OR predictions:", predict(X, w_or, b_or))

