import numpy as np
import matplotlib.pyplot as plt
from sklearn.datasets import make_blobs
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
from sklearn.cluster import KMeans
import tensorflow as tf
from tensorflow import keras

# ─────────────────────────────────────────
# 1. Génération des données
# ─────────────────────────────────────────
X, y = make_blobs(n_samples=300, centers=3, random_state=42, cluster_std=1.2)

plt.figure(figsize=(6, 4))
colors = ['red', 'blue', 'green']
for i in range(3):
    plt.scatter(X[y == i, 0], X[y == i, 1], c=colors[i],
                edgecolors='k', label=f'Cluster {i}')
plt.title("Données initiales - 3 clusters")
plt.legend()
plt.tight_layout()
plt.savefig("ex5_keras_data.png", dpi=100)
plt.show()

# ─────────────────────────────────────────
# 2. Prétraitement (clusters 0 et 1)
# ─────────────────────────────────────────
mask = y != 2
X_bin = X[mask]
y_bin = y[mask]

scaler = StandardScaler()
X_bin_scaled = scaler.fit_transform(X_bin)

X_train, X_test, y_train, y_test = train_test_split(
    X_bin_scaled, y_bin, test_size=0.2, random_state=42
)

# ─────────────────────────────────────────
# 3. Perceptron avec Keras
#    → Une seule couche Dense avec activation sigmoid (= Perceptron)
# ─────────────────────────────────────────
model = keras.Sequential([
    keras.layers.Input(shape=(2,)),
    keras.layers.Dense(1, activation='sigmoid')   # Perceptron simple
])

model.compile(
    optimizer=keras.optimizers.SGD(learning_rate=0.01),
    loss='binary_crossentropy',
    metrics=['accuracy']
)

model.summary()

history = model.fit(
    X_train, y_train,
    epochs=100,
    batch_size=16,
    validation_split=0.2,
    verbose=0
)

# Évaluation
loss, acc = model.evaluate(X_test, y_test, verbose=0)
print(f"\n[Keras] Loss : {loss:.4f} | Accuracy : {acc*100:.2f}%")

# Courbes d'apprentissage
fig, axes = plt.subplots(1, 2, figsize=(11, 4))
axes[0].plot(history.history['accuracy'], label='Train')
axes[0].plot(history.history['val_accuracy'], label='Validation')
axes[0].set_title("Accuracy par époque - Keras")
axes[0].set_xlabel("Époque"); axes[0].set_ylabel("Accuracy")
axes[0].legend()

axes[1].plot(history.history['loss'], label='Train')
axes[1].plot(history.history['val_loss'], label='Validation')
axes[1].set_title("Loss par époque - Keras")
axes[1].set_xlabel("Époque"); axes[1].set_ylabel("Loss")
axes[1].legend()

